package com.baselet.control;

public class DimensionDouble {

	private double height;
	private double width;

	public DimensionDouble(double width, double height) {
		super();
		this.width = width;
		this.height = height;
	}

	public double getWidth() {
		return width;
	}

	public double getHeight() {
		return height;
	}
}
