package com.baselet.element;

public class Dimension {
	public int width;
	public int height;
	
	public Dimension() {
	}
	
	public Dimension(int width, int height) {
		super();
		this.width = width;
		this.height = height;
	}

	public int getHeight() {
		return height;
	}
	
	public int getWidth() {
		return width;
	}
}
